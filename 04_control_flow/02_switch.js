const month_Num = 6;

switch (month_Num) {
    case 1:
        console.log(`Jannuary, 31 Day's Month`);
        break;
    case 2:
        console.log(`February, 28 Day's Month`);
        break;
    case 3:
        console.log(`March, 31 Day's Month`);
        break;
    case 4:
        console.log(`April, 30 Day's Month`);
        break;
    case 5:
        console.log(`May, 31 Day's Month`);
        break;
    case 6:
        console.log(`June, 30 Day's Month`);
        break;

    default:
        console.log(`please select number between 1 to 6!`);
        break;
}

